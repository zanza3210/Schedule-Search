    private void SmartScheduleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SmartScheduleButtonActionPerformed
        SmartScheduleTextArea.setText("");
        int y = 0;int x = 0;
        String text = ChooseSearchTextArea.getText();
        Scanner sc = new Scanner(text);
        while (sc.hasNextLine()) {sc.nextLine();x++;}
        String code[] = new String[x];String sec[] = new String[x];
        int day[] = new int[x];String month[] = new String[x];
        int year[] = new int[x];String time[] = new String[x];
        String saka[] = new String[x];int amount[] = new int[x];
        String room[] = new String[x];
        Scanner ac = new Scanner(text);
        while (ac.hasNext()) {
            code[y] = ac.next();
            sec[y] = ac.next();
            day[y] = ac.nextInt();
            month[y] = ac.next();
            year[y] = ac.nextInt();
            time[y] = ac.next();
            saka[y] = ac.next();
            amount[y] = ac.nextInt();
            room[y] = ac.next();
            y++;
        }

        for (int ii = y - 1; ii > 0; ii--) {
            for (int j = 0; j < ii; j++) {
                if (day[j] > day[j + 1]) {
                    int t = day[j];
                    day[j] = day[j + 1];
                    day[j + 1] = t;                 
                    String p = month[j];
                    month[j] = month[j + 1];
                    month[j + 1] = p;
                    int yy = year[j];
                    year[j] = year[j + 1];
                    year[j + 1] = yy;
                    String u = time[j];
                    time[j] = time[j + 1];
                    time[j + 1] = u;
                    String qq = code[j];
                    code[j] = code[j + 1];
                    code[j + 1] = qq;
                    String gg = room[j];
                    room[j] = room[j + 1];
                    room[j + 1] = gg; }            }        }
        for (int i = 0; i < y; i++) {
 SmartScheduleTextArea.append(day[i] + "/" + month[i] + "/" + year[i] + "          " 
         + time[i] + "          " + code[i] + "          " + room[i] + "\n");
        }
        jLabel8.setText(+y + " SUBJECT.");
    }//GEN-LAST:event_SmartScheduleButtonActionPerformed

