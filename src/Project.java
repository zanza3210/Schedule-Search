    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        ChooseSearchTextArea.setText("");
        if (!jLabel1.getText().equals("-------------------Please choose file----------------------------")) {
            try {
                Scanner sc = new Scanner(file);
                String selected = (String) ComboBox.getSelectedItem();
                while (sc.hasNextLine()) {
                    String myLine = sc.nextLine();
                    int index = myLine.indexOf(selected);
                    if (index != -1) {
                        ChooseSearchTextArea.append(myLine + "\n");
                    }
                }
                if (ChooseSearchTextArea.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Not found!!!!",
                            "Search Information", JOptionPane.ERROR_MESSAGE);

                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please choose file!!!!",
                    "File Information", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_SearchButtonActionPerformed

